# Ler uma variável numérica e imprimi-lá apenas se a mesma for maior que 100, caso
# contrário imprimi-lá com o valor zero.

#Entrada
num1 = float(input("Informe um valor: "))

#Processamento
if num1 > 100 :
    print(num1)

else:
    num1 = 0
    print(num1)
